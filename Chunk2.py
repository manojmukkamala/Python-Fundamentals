# IF Statement
life = 1;
if life:
    print ("Life Exists");
    print (life);
    
intelligence = 0;
if intelligence:
    print ("Intelligence not found");
    print (intelligence);

print ("Analysis Complete"); 


# IF ELSE Statements
life = 1;
if life:
    print ("Life Exists");
    print (life);
else:
    print ("Life doesnot Exists");
    print(life);
  
intelligence = 0;
if intelligence:
    print ("Intelligence found");
    print (intelligence);
else:
    print ("Intelligence not found");
    print (intelligence);

print ("Analysis Complete"); 

